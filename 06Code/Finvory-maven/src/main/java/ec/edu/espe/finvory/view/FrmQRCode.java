    public FrmQRCode(java.awt.Frame parent, boolean modal, String username, String secret) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(null);

        jLabel1.setText("Configuraci√≥n de Seguridad 2FA");
        lblKey.setText("<html><center>Clave manual:<br><b>" + secret + "</b></center></html>");

        try {
            String otpAuth = "otpauth://totp/" + username
                    + "?secret=" + secret
                    + "&issuer=Finvory";

            QRCodeWriter qrWriter = new QRCodeWriter();
            BitMatrix bitMatrix = qrWriter.encode(
                    otpAuth,
                    BarcodeFormat.QR_CODE,
                    200,
                    200
            );

            BufferedImage qrImage = MatrixToImageWriter.toBufferedImage(bitMatrix);
            lblQR.setIcon(new javax.swing.ImageIcon(qrImage));
            lblQR.setText("");

        } catch (Exception e) {
            lblQR.setText("Error al generar QR");
            e.printStackTrace();
        }
    }

