    private void loadCompanyData() {
        if (controller.getData() == null || controller.getData().getCompanyInfo() == null) {
            return;
        }

        CompanyAccount company = controller.getData().getCompanyInfo();

        txtCompanyName.setText(company.getName());
        txtRuc.setText(company.getRuc());
        txtPhone.setText(company.getPhone());
        txtEmail.setText(company.getEmail());
        passCompany.setText(company.getPassword());
        txtRuc.setEditable(false);
        txtUserName.setText(company.getUsername());
        txtUserName.setEditable(false);

        Address address = company.getAddress();
        if (address != null) {
            txtCountry.setText(address.getCountry());
            txtCity.setText(address.getCity());
            txtStreet.setText(address.getStreet());
            txtStreetNumber.setText(address.getStreetNumber());
            txtZipCode.setText(address.getZipCode());
            txtRegion.setText(address.getRegion());
        }

        txtTaxRate.setText(controller.getData().getTaxRate().toString());
        txtProfit.setText(controller.getData().getProfitPercentage().toString());
        txtDiscPremium.setText(controller.getData().getDiscountPremium().toString());
        txtDiscStandar.setText(controller.getData().getDiscountStandard().toString());
        txtDiscVip.setText(controller.getData().getDiscountVip().toString());
        txtTaxRate.setEditable(false);
        txtProfit.setEditable(false);
        txtDiscPremium.setEditable(false);
        txtDiscStandar.setEditable(false);
        txtDiscVip.setEditable(false);

        try {
            java.net.URL imgUrl = null;
            String path = company.getLogoPath();

            if (path != null && new java.io.File(path).exists()) {
                imgUrl = new java.io.File(path).toURI().toURL();
            } else {
                imgUrl = getClass().getResource("/personal.jpg");
            }
            int fixedWidth = 161;
            int fixedHeight = 152;
            if (imgUrl != null) {
                icon.setIcon(ValidationUtils.getScaledIcon(imgUrl, fixedWidth, fixedHeight));
            }
        } catch (Exception e) {
            System.err.println("Error en persistencia visual: " + e.getMessage());
        }
    }

