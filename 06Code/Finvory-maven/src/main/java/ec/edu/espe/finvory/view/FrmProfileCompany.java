    private void btnUploadPhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadPhotoActionPerformed
        CompanyAccount company = controller.getData().getCompanyInfo();

        company.setName(txtCompanyName.getText().trim());
        company.setPhone(txtPhone.getText().trim());
        company.setEmail(txtEmail.getText().trim());
        company.setPassword(new String(passCompany.getPassword()));

        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();

        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Imágenes", "jpg", "png", "jpeg"));

        if (fileChooser.showOpenDialog(this) == javax.swing.JFileChooser.APPROVE_OPTION) {
            java.io.File selectedFile = fileChooser.getSelectedFile();
            String newPath = controller.userController.handleUploadPhoto(selectedFile, company.getUsername(), company.getLogoPath());

            if (newPath != null) {
                company.setLogoPath(newPath);
                controller.saveData();
                loadCompanyData();
                JOptionPane.showMessageDialog(this, "Foto y datos actualizados correctamente.");
            }
        }
    }//GEN-LAST:event_btnUploadPhotoActionPerformed

