    private void btnSave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave1ActionPerformed
        String country = txtCountry.getText().trim();
        String city = txtCity.getText().trim();
        String street = txtStreet.getText().trim();
        String streetNumber = txtStreetNumber.getText().trim();
        String zipCode = txtZipCode.getText().trim();
        String region = txtRegion.getText().trim();

        StringBuilder errorMsg = new StringBuilder();

        if (ValidationUtils.isEmpty(country)) {
            errorMsg.append("- El país es obligatorio.\n");
        } else if (!ValidationUtils.isTextOnly(country)) {
            errorMsg.append("- El país solo debe contener letras.\n");
        }
        if (ValidationUtils.isEmpty(city)) {
            errorMsg.append("- La ciudad es obligatoria.\n");
        } else if (!ValidationUtils.isTextOnly(city)) {
            errorMsg.append("- La ciudad solo debe contener letras.\n");
        }
        if (ValidationUtils.isEmpty(street)) {
            errorMsg.append("- La calle es obligatoria.\n");
        }
        if (!ValidationUtils.isEmpty(region) && !ValidationUtils.isValidRegion(region)) {
            errorMsg.append("- La región solo debe contener letras.\n");
        }
        if (!ValidationUtils.isEmpty(streetNumber) && !ValidationUtils.isValidStreetNumber(streetNumber)) {
            errorMsg.append("- El número de calle contiene caracteres no válidos.\n");
        }
        if (errorMsg.length() == 0) {
            CompanyAccount company = controller.getData().getCompanyInfo();
            Address address = company.getAddress();
            if (address == null) {
                address = new Address(country, city, street);
                company.setAddress(address);
            }
            address.setCountry(country);
            address.setCity(city);
            address.setStreet(street);
            address.setStreetNumber(streetNumber);
            address.setZipCode(zipCode);
            address.setRegion(region);
            controller.saveData();

            JOptionPane.showMessageDialog(this, "Dirección de empresa actualizada con éxito.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Por favor corrija los siguientes errores:\n" + errorMsg.toString(),
                    "Error de Validación", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSave1ActionPerformed

